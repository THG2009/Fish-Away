package net.mcreator.fishawayforge.init;

import net.minecraftforge.common.loot.LootModifier;
import net.minecraftforge.common.loot.IGlobalLootModifier;

import net.minecraft.world.level.storage.loot.predicates.LootItemCondition;
import net.minecraft.world.level.storage.loot.LootContext;
import net.minecraft.world.item.ItemStack;
import net.minecraft.resources.ResourceLocation;

import net.mcreator.fishawayforge.FishAwayForgeMod;

import java.util.function.Supplier;

import it.unimi.dsi.fastutil.objects.ObjectArrayList;

import com.mojang.serialization.codecs.RecordCodecBuilder;
import com.mojang.serialization.Codec;

import com.google.common.base.Suppliers;

@Mod.EventBusSubscriber(modid = FishAwayForgeMod.MODID, bus = Mod.EventBusSubscriber.Bus.MOD)
public class FishAwayForgeModLootModifier {
	public static class FishAwayForgeModLootTableModifier extends LootModifier {
		public static final Supplier<Codec<FishAwayForgeModLootTableModifier>> CODEC = Suppliers
				.memoize(() -> RecordCodecBuilder.create(instance -> codecStart(instance).and(ResourceLocation.CODEC.fieldOf("lootTable").forGetter(m -> m.lootTable)).apply(instance, FishAwayForgeModLootTableModifier::new)));
		private final ResourceLocation lootTable;

		public FishAwayForgeModLootTableModifier(LootItemCondition[] conditions, ResourceLocation lootTable) {
			super(conditions);
			this.lootTable = lootTable;
		}

		@Override
		protected ObjectArrayList<ItemStack> doApply(ObjectArrayList<ItemStack> generatedLoot, LootContext context) {
			context.getResolver().getLootTable(lootTable).getRandomItems(context, generatedLoot::add);
			return generatedLoot;
		}

		@Override
		public Codec<? extends IGlobalLootModifier> codec() {
			return CODEC.get();
		}
	}

	public static final DeferredRegister<Codec<? extends IGlobalLootModifier>> LOOT_MODIFIERS = DeferredRegister.create(ForgeRegistries.Keys.GLOBAL_LOOT_MODIFIER_SERIALIZERS, "fish_away_forge");
	public static final RegistryObject<Codec<FishAwayForgeModLootTableModifier>> LOOT_MODIFIER = LOOT_MODIFIERS.register("fish_away_forge_loot_modifier", FishAwayForgeModLootTableModifier.CODEC);

	@SubscribeEvent
	public static void register(FMLConstructModEvent event) {
		IEventBus bus = FMLJavaModLoadingContext.get().getModEventBus();
		event.enqueueWork(() -> {
			LOOT_MODIFIERS.register(bus);
		});
	}
}
